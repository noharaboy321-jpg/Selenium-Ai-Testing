    @pytest.mark.xray(test_key='NES-14902')
    @pytest.mark.parametrize('nessus_type', [
        pytest.param("Nessus Professional", marks=pytest.mark.nessus_pro),
        pytest.param("Nessus Manager", marks=pytest.mark.nessus_manager)])
    def test_verify_product_summary_file_contents(self, nessus_type):
        """
        NES-13249 : [CLI-Automation] : Automate the new files added in bug-report-generator function
        NES-14902 : Verify .autoconfiguration.json file details.

        Scenario Tested:
            [x] Verify the information inside 'product_summary.txt' generated via bug-report-generator
        """
        self.generate_bug_report_and_extract_files()

        with SSH() as ssh:
            product_summary_file = ssh.execute("{} {}".format(get_command('display_content'), path_join(
                path_dir_list=[NessusCli.BugReportGenerator.BUG_REPORT_FILES_DIR,
                               NessusCli.BugReportGenerator.PRODUCT_SUMMARY])))

            # steps: Verify that all necessary information is present inside product summary file.
            if nessus_type == "Nessus Manager":
                assert [tag.split(":")[0].strip() for tag in product_summary_file if ":" in tag and tag !=
                        ""] == NessusCli.BugReportGenerator.PRODUCT_SUMMARY_TAGS_MANAGER, 'Product summery tags are incorrect.'
            else:
                assert [tag.split(":")[0].strip() for tag in product_summary_file if ":" in tag and tag !=
                        ""] == NessusCli.BugReportGenerator.PRODUCT_SUMMARY_TAGS_PRO, 'Product summary tags are incorrect.'

            # Verify product summery tags
            assert all([True for tag in product_summary_file if ":" in tag and "Features" not in tag and tag.split(
                ":")[1].strip() != ""]), "one or many product summary tags are empty."

            # Verify Nessus Type in product summary file.
            assert [tag.split(":")[1].strip() for tag in product_summary_file if "Name" in tag][0] == nessus_type, \
                "Product name is incorrect."

            output = ssh.execute("{} -v".format(get_nessus_cli()))[0]
            nessus_version = output[output.find(')') + 1:output.rfind('[')].strip(' ')

            # Verify Nessus Version in product summary file.
            assert [tag.split(":")[1].strip() for tag in product_summary_file if "Product" in tag][0] == \
                   nessus_version, "Product version is incorrect."
